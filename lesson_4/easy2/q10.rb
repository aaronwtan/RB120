# What are the benefits of using Object Oriented Programming in Ruby?
# Think of as many as you can.
# - OOP allows encapsulation of state and behavior into organized
# bundles, which allows for higher levels of design complexity
# and easier maintenance of the codebase
# - OOP allows controlled access to data and methods,
# so that implementation details and sensitive data can be hidden and restricted
# to only the parts of the code that need it,
# - creating objects allows programmers to think more abstractly about code
# - using nouns to represent objects and classes and verbs to represent
# behaviors allows code to be more easily modeled and conceptualized
# - it allows for functionality to easily be reused through our own
# and others' pre-written code